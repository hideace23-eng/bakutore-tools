async function callClaude(userMessage) {
  const today = new Date();
  const todayStr = today.getFullYear() + '年' + (today.getMonth() + 1) + '月' + today.getDate() + '日';
  const content = '今日の日付は' + todayStr + 'です。この日付を基準に、体験希望日の候補を正しい月・日で提示してください。\n\n以下の新規体験希望のお客様からのLINEに返信文を作成してください：\n\n' + userMessage;

  const res = await fetch('https://api.anthropic.com/v1/messages', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'x-api-key': process.env.ANTHROPIC_API_KEY,
      'anthropic-version': '2023-06-01',
    },
    body: JSON.stringify({
      model: 'claude-sonnet-4-20250514',
      max_tokens: 1000,
      system: SYSTEM,
      messages: [{ role: 'user', content }],
    }),
  });
  const data = await res.json();
  if (!res.ok) throw new Error(data?.error?.message || 'Claude API error');
  return data.content?.find((b) => b.type === 'text')?.text || '';
}

async function showLoading(userId) {
  await fetch('https://api.line.me/v2/bot/chat/loading/start', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      Authorization: 'Bearer ' + process.env.LINE_CHANNEL_ACCESS_TOKEN,
    },
    body: JSON.stringify({ chatId: userId, loadingSeconds: 30 }),
  });
}

async function replyToLine(replyToken, text) {
  await fetch('https://api.line.me/v2/bot/message/reply', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      Authorization: 'Bearer ' + process.env.LINE_CHANNEL_ACCESS_TOKEN,
    },
    body: JSON.stringify({
      replyToken,
      messages: [{ type: 'text', text }],
    }),
  });
}

export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return res.status(200).json({ status: 'ok' });
  }

  const events = (req.body && req.body.events) || [];

  for (const event of events) {
    if (event.type !== 'message' || event.message.type !== 'text') continue;
    const userMessage = event.message.text;
    const replyToken = event.replyToken;
    const userId = event.source.userId;

    try {
      await showLoading(userId);
      const reply = await callClaude(userMessage);
      await replyToLine(replyToken, reply);
    } catch (e) {
      console.error('Error:', e);
      await replyToLine(replyToken, '申し訳ありません、一時的にエラーが発生しました。少し時間をおいて再度お試しください。');
    }
  }

  return res.status(200).json({ status: 'ok' });
}
